let a = 7;
let b = 13;
let varMax;
// if else method
if(a > b){
    varMax = a;
}
else{
    varMax = b;
}
console.log("The value of Maximum number  = " + varMax);

// Ternary Operator

let p = 10;
let q = 20;
let varMax1;

p>q ? varMax1 = p : varMax1 = q;
console.log("The value of Maximum number by ternary is  = " + varMax1);